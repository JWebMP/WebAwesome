package com.jwebmp.webawesome.components.splitpanel;

/**
 * Defines the possible primary panel options for the WaSplitPanel component.
 * The primary panel maintains its size during host resize.
 */
public enum SplitPanelPrimary
{
    /**
     * The start panel is primary and maintains its size during host resize
     */
    Start,

    /**
     * The end panel is primary and maintains its size during host resize
     */
    End
}